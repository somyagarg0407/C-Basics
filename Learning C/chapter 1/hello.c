# include<stdio.h>

int main(){
    int age=25;
    float pi=3.1415;
    char hashtag='#';    //char =character stores single character like a or # or * etc
    printf("age is %d\n",age);  //use %d for int
    printf("value of pi is %f\n",pi);  //use %f for real number
    printf("sign of hashtag is %c\n",hashtag);  //use %c for character
    return 0;
} 
 

